<script>
function rejectOrder(button, orderCode) {
    const reason = prompt('Masukkan alasan penolakan untuk order ' + orderCode + ':');

    if (reason !== null && reason.trim() !== '') {
        if (confirm('Apakah Anda yakin ingin menolak order ini?')) {
            // Set the rejection reason in the hidden input
            const form = button.closest('form');
            const reasonInput = form.querySelector('input[name="rejection_reason"]');
            reasonInput.value = reason.trim();

            // Submit the form
            form.submit();
        }
    } else if (reason !== null) {
        alert('Alasan penolakan wajib diisi.');
    }
}
</script>
